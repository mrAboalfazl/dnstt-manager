package service

import (
	"encoding/base64"
	"encoding/json"
	"fmt"
	"os"
	"strings"

	"github.com/mrAboalfazl/dnstt-manager/config"
	"github.com/mrAboalfazl/dnstt-manager/models"
)

type ClientConfig struct {
	Domain      string `json:"domain"`
	PubKey      string `json:"pubkey"`
	SSHUser     string `json:"ssh_user"`
	SSHPass     string `json:"ssh_pass"`
	SSHPort     int    `json:"ssh_port"`
	DNSResolver string `json:"dns_resolver"`
	MTU         int    `json:"mtu"`
	ServerIP    string `json:"server_ip"`
}

func GenerateUserConfig(user *models.User, plainPassword string) (*ClientConfig, error) {
	cfg := config.Get()

	pubkey := ""
	if data, err := os.ReadFile(cfg.PubKeyFile); err == nil {
		pubkey = strings.TrimSpace(string(data))
	}

	return &ClientConfig{
		Domain:      cfg.DNSTTDomain,
		PubKey:      pubkey,
		SSHUser:     user.Username,
		SSHPass:     plainPassword,
		SSHPort:     cfg.SSHPort,
		DNSResolver: cfg.DNSResolver,
		MTU:         cfg.MTU,
		ServerIP:    cfg.ServerIP,
	}, nil
}


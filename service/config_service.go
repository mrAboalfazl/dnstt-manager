func GenerateLink(user *models.User) (string, error) {
	cfg := config.Get()

	pubkey := ""
	if data, err := os.ReadFile(cfg.PubKeyFile); err == nil {
		pubkey = strings.TrimSpace(string(data))
	}

	linkData := map[string]interface{}{
		"domain":       cfg.DNSTTDomain,
		"pubkey":       pubkey,
		"ssh_user":     user.Username,
		"ssh_port":     cfg.SSHPort,
		"dns_resolver": cfg.DNSResolver,
		"mtu":          cfg.MTU,
		"server_ip":    cfg.ServerIP,
	}

	jsonBytes, err := json.Marshal(linkData)
	if err != nil {
		return "", err
	}

	encoded := base64.URLEncoding.EncodeToString(jsonBytes)
	return fmt.Sprintf("dnstt://%s", encoded), nil
}

func GenerateConfigFile(user *models.User) (string, error) {
	cfg := config.Get()

	pubkey := ""
	if data, err := os.ReadFile(cfg.PubKeyFile); err == nil {
		pubkey = strings.TrimSpace(string(data))
	}

	content := fmt.Sprintf(`# DNSTT Configuration for %s
# Generated by DNSTT Manager
#
# This configuration connects through DNS tunnel to the server.

[tunnel]
domain = %s
pubkey = %s
mtu = %d
dns_resolver = %s

[ssh]
host = 127.0.0.1
port = %d
username = %s

[server]
ip = %s

# Client command example (Desktop):
# ./dnstt-client -doh %s -pubkey %s %s 127.0.0.1:7000
# Then: ssh -D 1080 -p %d %s@127.0.0.1
`,
		user.Username,
		cfg.DNSTTDomain,
		pubkey,
		cfg.MTU,
		cfg.DNSResolver,
		cfg.SSHPort,
		user.Username,
		cfg.ServerIP,
		cfg.DNSResolver,
		pubkey,
		cfg.DNSTTDomain,
		cfg.SSHPort,
		user.Username,
	)

	return content, nil
}
